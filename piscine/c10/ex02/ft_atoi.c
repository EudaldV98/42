int		ft_atoi(char *str)
{
	long long int	nb;
	int				neg;

	nb = 0;
	neg = 0;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			return (-1);
		str++;
	}
	while ((*str >= '0' && *str <= '9'))
		nb = (nb * 10) + *str++ - 48;
	if (neg % 2 != 0)
		nb = -nb;
	if (*str)
		return (-1);
	return (nb);
}
