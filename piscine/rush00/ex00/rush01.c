void	ft_putchar(char c);

void	rush(int x, int y)
{
	int i;
	int j;

	i = 1;
	while (i <= y)
	{
		j = 1;
		while (j <= x)
		{
			if ((i == 1 && j == 1) || ((i == y && i > 1) && (j == x && j > 1)))
				ft_putchar('/');
			else if (((i == 1 || (i == y)) && (j > 1 && j < x))
					|| ((i > 1 && i < y) && (j == 1 || j == x)))
				ft_putchar('*');
			else if ((i == 1 && (j == x)) || (i == y && j == 1))
				ft_putchar('\\');
			else if ((i > 1 || i < y) && (j > 1 && j < x))
				ft_putchar(32);
			j++;
		}
		if (y > 0 && x > 0)
			ft_putchar('\n');
		i++;
	}
}
