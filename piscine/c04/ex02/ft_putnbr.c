#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putnbr(int nb)
{
	if (nb > 9 || nb < -9)
		ft_putnbr(nb / 10);
	else if (nb < 0)
		ft_putchar('-');
	ft_putchar(nb > 0 ? nb % 10 + 48 : -(nb % 10) + 48);
}
