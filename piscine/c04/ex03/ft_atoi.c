int		ft_atoi(char *str)
{
	int nb;
	int neg;

	nb = 0;
	neg = 0;
	while (*str == 32 || *str == '\t' || *str == '\n'
			|| *str == '\v' || *str == '\f' || *str == '\r')
		str++;
	while (*str == '+' || *str == '-')
	{
		if (*str == '-')
			neg++;
		str++;
	}
	while ((*str >= '0' && *str <= '9'))
		nb = (nb * 10) + *str++ - 48;
	if (neg % 2 != 0)
		nb = -nb;
	return (nb);
}
