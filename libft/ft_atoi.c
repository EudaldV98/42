#include "libft.h"

int		ft_atoi(const char *str)
{
	int nb;
	int neg;

	nb = 0;
	neg = 0;
	while (*str == 32 || (*str >= 9 && *str <= 13))
		str++;
	if (*str == '+' || *str == '-')
	{
		if (*str == '-')
			neg++;
		str++;
	}
	while ((*str >= '0' && *str <= '9'))
		nb = (nb * 10) + *str++ - 48;
	if (neg == 1)
		nb = -nb;
	return (nb);
}
